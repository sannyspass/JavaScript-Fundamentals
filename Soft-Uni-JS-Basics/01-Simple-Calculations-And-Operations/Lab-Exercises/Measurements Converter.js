/*4. От инчове към сантиметри.
Да се напише програма, която изважда цяло число от аргумента input и преобразува числото от инчове в сантиметри. 
За целта умножава инчовете по 2.54 (защото 1 инч = 2.54 сантиметра).*/

function convert(input) {
  let inch = input;
  let cm = inch * 2.54;
  console.log(`${inch} inch is equal to ${cm} cm.`);
}

convert(1);
